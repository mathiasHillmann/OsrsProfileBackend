    public function translate(array &$data): void
    {
        $achieviements = $this->getValuesToTrack();

        $index = 0;

        foreach ($achieviements as $CATag => $achievement) {
            $item = &$data[$CATag];

            for ($i = 0; $i < 32; $i++) {
                $completed = null;

                if ($item) {
                    $completed = $this->isBitSet((string) $item['value'], $i);
                }

                $task = self::TASKS[32 * $index + $i] ?? null;

                if ($task) {
                    $data['tasks'][] = [
                        ...$task,
                        'completed' => $completed,
                    ];
                }
            }

            $index++;
            unset($item);
            unset($data[$CATag]);
        }
    }

